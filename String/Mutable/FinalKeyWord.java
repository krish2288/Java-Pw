public class FinalKeyWord {
    public static void main(String[] args) {
        final int a=54;
        // final keyword make value not change
        // if we try change value of a we cann't change
        // ex a=98;
        final StringBuffer s1=new StringBuffer("krish");
        s1.append(" singh");
        System.out.println(s1);
    }
}
